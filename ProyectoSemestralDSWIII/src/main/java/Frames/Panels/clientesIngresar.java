    private void guardarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarButtonActionPerformed
        CrearDatos mJdbc = new CrearDatos();
        Cliente cliente = new Cliente();
        
        cliente.setNombre(nombreCampo.getText());
        cliente.setCedula(cedulaCampo.getText());
        cliente.setFecha_nacimiento(nacimientoCampo.getText());
        cliente.setGenero(generoCampo.getSelectedItem().toString());
        cliente.setCorreo(correoCampo.getText());
        cliente.setTelefono(telefonoCampo.getText());
        cliente.setProvincia(provinciaCampo.getText());
        cliente.setCiudad(ciudadCampo.getText());
        cliente.setCorregimiento(corregimientoCampo.getText());
        cliente.setTipo_cliente(estadoCampo.getSelectedItem().toString());
        
        mJdbc.crear(cliente);
        
    }//GEN-LAST:event_guardarButtonActionPerformed

